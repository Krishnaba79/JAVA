class Length
{
	public static void main(String[] args)
	{

		//return the length of string
		System.out.println("GeeksforGeeks".length());

		//return character
		System.out.println("GeeksforGeeks".charAt(3));

		//return substring from ith index character to end
		System.out.println("GeeksforGeeks".substring(3));

		//return substring from i to j index
		System.out.println("GeeksforGeeks".substring(2,5));
	}
}